interface SidebarProps {
  authed: boolean;
  mobileOpen?: boolean;
  onCloseMobile?: () => void;
}

export default function Sidebar({ authed, mobileOpen, onCloseMobile }: SidebarProps) {
  const pathname = usePathname();
  const [collapsed, setCollapsed] = React.useState(false);

  const isActive = (href: string) =>
    pathname === href || (href !== "/" && pathname.startsWith(href + "/"));

  return (
    <>
      {/* Mobile overlay */}
      <div
        className={`sidebarOverlay ${mobileOpen ? "open" : ""}`}
        onClick={onCloseMobile}
        role="button"
        tabIndex={-1}
        aria-label="Sidebar schliessen"
        onKeyDown={(e) => e.key === "Escape" && onCloseMobile?.()}
      />

      <aside className={`sidebar ${collapsed ? "collapsed" : ""} ${mobileOpen ? "mobileOpen" : ""}`}>
        <div className="brand">
          <div className="logo">CS</div>
          {!collapsed && (
            <div>
              <div className="brandTitle">CoffeeStudio</div>
              <div className="brandSub">Operations Hub</div>
            </div>
          )}
        </div>

        <button
          className="ghost"
          onClick={() => setCollapsed((v) => !v)}
          title={collapsed ? "Sidebar erweitern" : "Sidebar minimieren"}
        >
          {collapsed ? ">>" : "<<"}
        </button>

        <nav className="nav">
          {navGroups.map((group, groupIndex) => (
            <div key={group.label} className="navGroup">
              {!collapsed && <div className="navGroupLabel">{group.label}</div>}
              <div className="navGroupItems">
                {group.items.map((item) => (
                  <Link
                    key={item.href}
                    href={authed ? item.href : "/login"}
                    className={`navItem ${isActive(item.href) ? "active" : ""}`}
                    onClick={() => onCloseMobile?.()}
                  >
                    <NavIcon name={item.icon} />
                    {!collapsed && <span>{item.label}</span>}
                  </Link>
                ))}
              </div>
              {!collapsed && groupIndex < navGroups.length - 1 && <div className="navDivider" />}
            </div>
          ))}
        </nav>

        <div className="sidebarFooter">
          {!collapsed && (
            <div className="muted small">
              v0.2.0 | Operations Platform
            </div>
          )}
        </div>
      </aside>
    </>
  );
}
